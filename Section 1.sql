-- This statement creates a table named 'flight1' to store flight information.

CREATE TABLE `flight1` (
  -- Unique tail number of the aircraft operating the flight.
  `TAIL_NUMBER` VARCHAR(255) NOT NULL,
  -- Airline operating the flight (reference to airlines table).
  `AIRLINE` VARCHAR(255) NOT NULL,
  -- Airport the flight departs from (reference to airports table).
  `ORIGIN_AIRPORT` VARCHAR(255) NOT NULL,
  -- Airport the flight arrives at (reference to airports table).
  `DESTINATION_AIRPORT` VARCHAR(255) NOT NULL,
  -- Scheduled departure time in epoch format (may be null).
  `SCHEDULED_DEPARTURE` int(11) DEFAULT NULL,
  -- Actual departure time in epoch format (may be null).
  `DEPARTURE_TIME` int(11) DEFAULT NULL,
  -- Departure delay in minutes (may be null).
  `DEPARTURE_DELAY` int(11) DEFAULT NULL,
  -- Scheduled arrival time in epoch format (may be null).
  `SCHEDULED_ARRIVAL` int(11) DEFAULT NULL,
  -- Actual arrival time in epoch format (may be null).
  `ARRIVAL_TIME` int(11) NOT NULL,
  -- Arrival delay in minutes
  `ARRIVAL_DELAY` int(11) NOT NULL,
  -- Reason for cancellation if applicable (reference to cancellation_codes table).
  `CANCELLATION_REASON` VARCHAR(255) NOT NULL,
  -- Defines TAIL_NUMBER as the primary key for unique flight identification.
  PRIMARY KEY (TAIL_NUMBER),
  -- Foreign key constraint referencing IATA_CODE in airports table for origin airport.
  FOREIGN KEY (ORIGIN_AIRPORT) REFERENCES airports(IATA_CODE),
  -- Foreign key constraint referencing IATA_CODE in airports table for destination airport.
  FOREIGN KEY (DESTINATION_AIRPORT) REFERENCES airports(IATA_CODE),
  -- Foreign key constraint referencing IATA_CODE in airlines table for operating airline.
  FOREIGN KEY (AIRLINE) REFERENCES airlines(IATA_CODE),
  -- Foreign key constraint referencing CANCELLATION_REASON in cancellation_codes table.
  FOREIGN KEY (CANCELLATION_REASON) REFERENCES cancellation_codes(CANCELLATION_REASON)
);

